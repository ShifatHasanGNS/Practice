from math import log10

def nthPalindrome_1(n):
    r = int(log10(n))
    s = (2*(10**r) - 2)//n
    t = r - s
    d = 1 + (2*t) + n//((11*(10**t)) - 1)
    a = d % 2
    m = (n + 1) + (9*a - 10)*(10**((d - 1)//2))
    return (m*(10**(d//2)) + int(str(m//(10**a))[::-1]))

def nthPalindrome_2(n):
    p = 10**(int(log10(n)) - 1)
    if n > (11*p-2): p = int(p * 10)
    m = (n + 1) - p
    s = str(m)
    return int(s + s[-1 if m < p else -2::-1])

